export const getMockFlashcardsByCategory = (categoryName: string): Flashcard[] => {
  return mockFlashcards.filter(flashcard => flashcard.category === categoryName);
};

export const getMockFlashcardById = (id: string): Flashcard | undefined => {
  return mockFlashcards.find(flashcard => flashcard.id === id);
};

export const getFlashcardStatistics = () => {
  const totalFlashcards = mockFlashcards.length;
  const totalCategories = mockCategories.length;
  const aiGeneratedFlashcards = mockFlashcards.filter(card => card.isAIGenerated).length;
  
  const difficultyStats = {
    easy: mockFlashcards.filter(card => card.difficulty === 'Easy').length,
    medium: mockFlashcards.filter(card => card.difficulty === 'Medium').length,
    hard: mockFlashcards.filter(card => card.difficulty === 'Hard').length
  };

  return {
    totalFlashcards,
    totalCategories,
    aiGeneratedFlashcards,
    difficultyStats
  };
};